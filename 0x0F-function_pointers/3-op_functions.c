#include "3-calc.h"

/**
 * op_add - op_add addition
 * @a: int
 * @b: int
 * Return: total
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - op_sub substraction
 * @a: int
 * @b: int
 * Return: total
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - op_mul multiplication
 * @a: int
 * @b: int
 * Return: total
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - op_div division
 * @a: int
 * @b: int
 * Return: total
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - op_mod modulo
 * @a: int
 * @b: int
 * Return: total
 */
int op_mod(int a, int b)
{
	return (a % b);
}

